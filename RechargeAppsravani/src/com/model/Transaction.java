package com.model;

public class Transaction {

private int tid;
private int mobno;
private int money;
private String servicenam;

public Transaction()
{
	
}

public int getTid() {
	return tid;
}

public void setTid(int tid) {
	this.tid = tid;
}

public int getMobno() {
	return mobno;
}

public void setMobno(int mobno) {
	this.mobno = mobno;
}

public int getMoney() {
	return money;
}

public void setMoney(int money) {
	this.money = money;
}

public String getServicenam() {
	return servicenam;
}

public void setServicenam(String servicenam) {
	this.servicenam = servicenam;
}





}
